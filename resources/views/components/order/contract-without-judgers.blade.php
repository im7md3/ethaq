        - لا يُعد من القوة القاهرة تأخر التَّنفيذ بسبب تقصير أيٍّ من أطراف العقد أو عدم الكفاءة في العمل ما لم يكن النقص
        في ذلك من القوة القاهرة.
        - في حال حصول قوة قاهرة تتعلق بالمنصة على سبيل المثال لا الحصر انقطاع شبكات الاتصال، أو الاختراقات التقنية
        للمنصة، أو تعطل استخدامها لأسباب خارجة عن إرادتها، سوف يتم إشعار الأطراف عبر وسائل التواصل للمنصة بالطرق البديلة
        حال توفرها.
