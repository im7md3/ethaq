        المادة التاسعة: القوة القاهرة
        - القوة القاهرة هي الحدث العام الذي يخرج عن سيطرة أطراف العقد ولا يمكن توقعه ويستحيل دفعه كما يستحيل تنفيذ
        التزامات المتعاقد أثناء قيامها، ولا يعزى لتسبب أو خطأ أو إهمال من أحد الأطراف أو أي شخص آخر، ويشمل -على سبيل
        المثال لا الحصر- الحريق والفيضان والحوادث والحرب والعمليات العسكرية والحظر الاقتصادي، وانقطاع شبكات الاتصال،
