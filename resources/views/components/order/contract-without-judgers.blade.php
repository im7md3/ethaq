        المتفق عليه، ويجب على المتعاقد في حال التأخر عن تنفيذ الأعمال بسبب القوة القاهرة إخطار الطرف الآخر في حينها بحقه
        في إنهاء العقد أو تأجيل تنفيذه بالاتفاق بينهما إذا أصبح تنفيذ الأعمال مستحيلًا لاستمرار القوة القاهرة لمدة
        تتجاوز يومي عمل.

